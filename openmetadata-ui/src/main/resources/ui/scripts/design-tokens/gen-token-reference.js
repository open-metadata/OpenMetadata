/**
 * Generates specs/tokens/token-reference.md — a master map of every Layer 2
 * (--om-*) token, its resolved value, and a group-level "when to use" note.
 * Parses src/styles/tokens.css so the reference can never drift from reality.
 *
 * Run: node scripts/design-tokens/gen-token-reference.js
 */
const fs = require('fs');
const path = require('path');

const UI = path.resolve(__dirname, '../..');
const TOKENS = path.join(UI, 'src/styles/tokens.css');
const OUT = path.join(UI, 'specs/tokens/token-reference.md');

function parseDefs(rawCss) {
  // Strip comments first — a commented example like
  // `--om-color-text: var(--ds-color-text-primary, #181d27)` (no `;`) would
  // otherwise be captured as a bogus definition.
  const css = rawCss.replace(/\/\*[\s\S]*?\*\//g, '');
  const defs = new Map();
  const re = /(--[\w-]+)\s*:\s*([^;]+);/g;
  let m;
  while ((m = re.exec(css))) {
    if (!defs.has(m[1])) {
      defs.set(m[1], m[2].trim());
    }
  }
  return defs;
}

// Resolve a token to a concrete value by following var() fallbacks/references.
function resolve(name, defs, seen = new Set()) {
  let out = name;
  if (!seen.has(name) && defs.has(name)) {
    seen.add(name);
    const def = defs.get(name);
    const varMatch = def.match(/^var\(\s*(--[\w-]+)\s*(?:,\s*([\s\S]+))?\)$/);
    if (varMatch) {
      const ref = varMatch[1];
      const fallback = varMatch[2];
      if (defs.has(ref)) {
        out = resolve(ref, defs, seen);
      } else if (fallback) {
        out = fallback.trim();
      } else {
        out = `var(${ref})`;
      }
    } else {
      out = def;
    }
  }
  return out;
}

const GROUPS = [
  { key: /^--om-space-/, title: 'Spacing', note: 'padding / margin / gap. See foundations/spacing.md.' },
  { key: /^--om-radius-/, title: 'Radius', note: 'border-radius. See foundations/radius.md.' },
  { key: /^--om-font-size-/, title: 'Font size', note: 'font-size. See foundations/typography.md.' },
  { key: /^--om-font-weight-/, title: 'Font weight', note: 'font-weight.' },
  { key: /^--om-font-|^--om-line-height-/, title: 'Font family & line height', note: '' },
  { key: /^--om-shadow-/, title: 'Elevation', note: 'box-shadow. See foundations/elevation.md.' },
  { key: /^--om-z-/, title: 'z-index', note: 'stacking. Prefer the semantic ladder for new work.' },
  { key: /^--om-duration-|^--om-ease-/, title: 'Motion', note: 'transition / animation. See foundations/motion.md.' },
  { key: /^--om-color-(text|bg|border|fg|link|interactive|focus)/, title: 'Semantic colors', note: 'Prefer these — they adapt to dark mode.' },
  { key: /^--om-color-(white|black|transparent)/, title: 'Absolute colors', note: '' },
  { key: /^--om-color-/, title: 'Palette colors', note: 'Fixed swatches; do NOT adapt to dark mode. Prefer semantic tokens.' },
  { key: /^--om-legacy-color-/, title: 'Legacy colors', note: 'Exact migrated one-offs (migration debt). Do not use in new code; re-express with a semantic token.' },
];

function classify(name) {
  return GROUPS.findIndex((g) => g.key.test(name));
}

function main() {
  const css = fs.readFileSync(TOKENS, 'utf8');
  const defs = parseDefs(css);
  const omNames = [...defs.keys()].filter((n) => n.startsWith('--om-'));

  const buckets = GROUPS.map(() => []);
  for (const name of omNames) {
    const gi = classify(name);
    if (gi >= 0) {
      buckets[gi].push(name);
    }
  }

  let md =
    '# Token reference\n\n' +
    '> Generated by `node scripts/design-tokens/gen-token-reference.js` from ' +
    '`src/styles/tokens.css`. Do not edit by hand.\n\n' +
    'Master map of every **Layer 2 (`--om-*`) token** — the tokens components ' +
    'reference. Each resolves through a Layer 1 `--ds-*` primitive to the ' +
    'value shown. Full layering: [../README.md](../README.md).\n\n' +
    `Total Layer 2 tokens: **${omNames.length}**.\n`;

  GROUPS.forEach((g, gi) => {
    const names = buckets[gi];
    if (!names.length) {
      return;
    }
    const legacy = /Legacy|Palette/.test(g.title);
    names.sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));
    md += `\n## ${g.title} (${names.length})\n\n`;
    if (g.note) {
      md += `${g.note}\n\n`;
    }
    if (legacy && names.length > 40) {
      md += '<details><summary>Show all ' + names.length + '</summary>\n\n';
    }
    md += '| Token | Value |\n| --- | --- |\n';
    for (const name of names) {
      md += `| \`${name}\` | \`${resolve(name, defs)}\` |\n`;
    }
    if (legacy && names.length > 40) {
      md += '\n</details>\n';
    }
  });

  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, md);
  process.stdout.write(
    `Wrote ${OUT} (${omNames.length} tokens across ${
      buckets.filter((b) => b.length).length
    } groups)\n`
  );
}

main();
