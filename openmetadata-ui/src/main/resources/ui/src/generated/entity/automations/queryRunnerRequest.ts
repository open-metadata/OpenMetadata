/**
 * Query Runner Request
 */
export interface QueryRunnerRequest {
    /**
     * Authentication type configured by admin in QueryRunnerConfig (e.g., Basic, ExternalOAuth,
     * etc.). Used to determine which credential field to use from user config.
     */
    authType?: string;
    /**
     * Type of the connection to test such as Snowflake, MySQL, Looker, etc.
     */
    connectionType?: string;
    /**
     * Source type of the resolved credentials. Indicates whether the credentials come from a
     * user-level config or a team-level config. Set by the backend during credential resolution.
     */
    credentialSourceType?: CredentialSourceType;
    /**
     * Optional database/dataset to use for query execution (selected by user in QueryRunner
     * Studio). Service-specific name (e.g., Snowflake database).
     */
    database?: string;
    /**
     * Optional database schema to use for query execution (selected by user in QueryRunner
     * Studio). Service-specific name (e.g., Snowflake schema). Named 'databaseSchema' instead
     * of 'schema' to avoid conflicts with Pydantic's BaseModel.schema() method.
     */
    databaseSchema?: string;
    /**
     * RUNTIME FIELD - Automatically injected by backend from admin
     * QueryRunnerConfig.querySettings.maxResultSize. This is NOT user-configurable in the
     * request. The backend fetches this value from the service's QueryRunnerConfig and injects
     * it here for enforcement by the Python workflow. If query has LIMIT exceeding this value,
     * an error is raised. If query has no LIMIT, one is automatically injected.
     */
    maxResultSize?: number;
    /**
     * Query to be executed.
     */
    query?: string;
    /**
     * RUNTIME FIELD - Full S3/GCS key path where the worker should upload CSV results.
     * Generated by the backend before Argo submission.
     */
    resultPath?: string;
    /**
     * Optional role to use for query execution (selected by user in QueryRunner Studio).
     * Service-specific (e.g., Snowflake role).
     */
    role?: string;
    /**
     * Optional value that identifies this service name.
     */
    serviceName?: string;
    /**
     * RUNTIME FIELD - Storage configuration injected by backend for result upload.
     */
    storageConfig?: StorageConfig;
    /**
     * Optional value to indicate if the query should be transpiled.
     */
    transpile?: boolean;
    /**
     * UUID of the user's QueryRunner config for this specific service (looked up by backend
     * using userId + serviceName)
     */
    userConfigId?: string;
    /**
     * UUID of the user executing the query (extracted from JWT token in backend)
     */
    userId?: string;
    /**
     * Optional value of the workflow name responsible for running the test
     */
    workflowName?: string;
}

/**
 * Source type of the resolved credentials. Indicates whether the credentials come from a
 * user-level config or a team-level config. Set by the backend during credential resolution.
 */
export enum CredentialSourceType {
    Team = "team",
    User = "user",
}

/**
 * RUNTIME FIELD - Storage configuration injected by backend for result upload.
 */
export interface StorageConfig {
    /**
     * S3 or GCS bucket name.
     */
    bucketName?: string;
    /**
     * Key prefix within the bucket.
     */
    prefix?:        string;
    storageConfig?: Credentials;
    [property: string]: any;
}

/**
 * AWS credentials configs.
 *
 * GCP credentials configs.
 */
export interface Credentials {
    /**
     * The Amazon Resource Name (ARN) of the role to assume. Required Field in case of Assume
     * Role
     */
    assumeRoleArn?: string;
    /**
     * An identifier for the assumed role session. Use the role session name to uniquely
     * identify a session when the same role is assumed by different principals or for different
     * reasons. Required Field in case of Assume Role
     */
    assumeRoleSessionName?: string;
    /**
     * The Amazon Resource Name (ARN) of the role to assume. Optional Field in case of Assume
     * Role
     */
    assumeRoleSourceIdentity?: string;
    /**
     * AWS Access key ID.
     */
    awsAccessKeyId?: string;
    /**
     * AWS Region
     */
    awsRegion?: string;
    /**
     * AWS Secret Access Key.
     */
    awsSecretAccessKey?: string;
    /**
     * AWS Session Token.
     */
    awsSessionToken?: string;
    /**
     * Enable AWS IAM authentication. When enabled, uses the default credential provider chain
     * (environment variables, instance profile, etc.). Defaults to false for backward
     * compatibility.
     */
    enabled?: boolean;
    /**
     * EndPoint URL for the AWS
     */
    endPointURL?: string;
    /**
     * The name of a profile to use with the boto session.
     */
    profileName?: string;
    /**
     * We support two ways of authenticating to GCP i.e via GCP Credentials Values or GCP
     * Credentials Path
     */
    gcpConfig?: GCPCredentialsConfiguration;
    /**
     * we enable the authenticated service account to impersonate another service account
     */
    gcpImpersonateServiceAccount?: GCPImpersonateServiceAccountValues;
}

/**
 * We support two ways of authenticating to GCP i.e via GCP Credentials Values or GCP
 * Credentials Path
 *
 * Pass the raw credential values provided by GCP
 *
 * Pass the path of file containing the GCP credentials info
 *
 * Use the application default credentials
 */
export interface GCPCredentialsConfiguration {
    /**
     * Google Cloud auth provider certificate.
     */
    authProviderX509CertUrl?: string;
    /**
     * Google Cloud auth uri.
     */
    authUri?: string;
    /**
     * Google Cloud email.
     */
    clientEmail?: string;
    /**
     * Google Cloud Client ID.
     */
    clientId?: string;
    /**
     * Google Cloud client certificate uri.
     */
    clientX509CertUrl?: string;
    /**
     * Google Cloud private key.
     */
    privateKey?: string;
    /**
     * Google Cloud private key id.
     */
    privateKeyId?: string;
    /**
     * Project ID
     *
     * GCP Project ID to parse metadata from
     */
    projectId?: string[] | string;
    /**
     * Google Cloud token uri.
     */
    tokenUri?: string;
    /**
     * Google Cloud Platform account type.
     *
     * Google Cloud Platform ADC ( Application Default Credentials )
     */
    type?: string;
    /**
     * Path of the file containing the GCP credentials info
     */
    path?: string;
    /**
     * Google Security Token Service audience which contains the resource name for the workload
     * identity pool and the provider identifier in that pool.
     */
    audience?: string;
    /**
     * This object defines the mechanism used to retrieve the external credential from the local
     * environment so that it can be exchanged for a GCP access token via the STS endpoint
     */
    credentialSource?: { [key: string]: string };
    /**
     * Google Cloud Platform account type.
     */
    externalType?: string;
    /**
     * Google Security Token Service subject token type based on the OAuth 2.0 token exchange
     * spec.
     */
    subjectTokenType?: string;
    /**
     * Google Security Token Service token exchange endpoint.
     */
    tokenURL?: string;
    [property: string]: any;
}

/**
 * we enable the authenticated service account to impersonate another service account
 *
 * Pass the values to impersonate a service account of Google Cloud
 */
export interface GCPImpersonateServiceAccountValues {
    /**
     * The impersonated service account email
     */
    impersonateServiceAccount?: string;
    /**
     * Number of seconds the delegated credential should be valid
     */
    lifetime?: number;
    [property: string]: any;
}
