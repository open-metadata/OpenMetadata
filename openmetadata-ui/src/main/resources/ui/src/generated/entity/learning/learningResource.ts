/**
 * A learning resource such as an in-product tutorial, Storylane walkthrough, or expert
 * video contextualized for product surfaces.
 */
export interface LearningResource {
    /**
     * Topic categories the resource belongs to.
     */
    categories:         ResourceCategory[];
    changeDescription?: ChangeDescription;
    /**
     * List of contexts where this resource should surface.
     */
    contexts: ResourceContext[];
    /**
     * Soft delete marker.
     */
    deleted?:     boolean;
    description?: string;
    difficulty?:  ResourceDifficulty;
    /**
     * Display label presented to users.
     */
    displayName?: string;
    /**
     * Approximate viewing time in seconds.
     */
    estimatedDuration?:            number;
    extension?:                    any;
    followers?:                    EntityReference[];
    fullyQualifiedName?:           string;
    href?:                         string;
    id:                            string;
    impersonatedBy?:               string;
    incrementalChangeDescription?: ChangeDescription;
    name:                          string;
    /**
     * Owners accountable for maintaining this resource.
     */
    owners?:      EntityReference[];
    resourceType: ResourceType;
    reviewers?:   EntityReference[];
    source:       ResourceSource;
    /**
     * Lifecycle state for controllable publishing.
     */
    status?: Status;
    /**
     * Tags attached to the learning resource.
     */
    tags?:      TagLabel[];
    updatedAt?: number;
    updatedBy?: string;
    version?:   number;
}

/**
 * Primary topic grouping for the resource.
 */
export enum ResourceCategory {
    Administration = "Administration",
    DataGovernance = "DataGovernance",
    DataQuality = "DataQuality",
    Discovery = "Discovery",
    Observability = "Observability",
}

/**
 * Description of the change.
 */
export interface ChangeDescription {
    changeSummary?: { [key: string]: ChangeSummary };
    /**
     * Names of fields added during the version changes.
     */
    fieldsAdded?: FieldChange[];
    /**
     * Fields deleted during the version changes with old value before deleted.
     */
    fieldsDeleted?: FieldChange[];
    /**
     * Fields modified during the version changes with old and new values.
     */
    fieldsUpdated?: FieldChange[];
    /**
     * When a change did not result in change, this could be same as the current version.
     */
    previousVersion?: number;
}

export interface ChangeSummary {
    changedAt?: number;
    /**
     * Name of the user or bot who made this change
     */
    changedBy?:    string;
    changeSource?: ChangeSource;
    [property: string]: any;
}

/**
 * The source of the change. This will change based on the context of the change (example:
 * manual vs programmatic)
 */
export enum ChangeSource {
    Automated = "Automated",
    Derived = "Derived",
    Ingested = "Ingested",
    Manual = "Manual",
    Propagated = "Propagated",
    Suggested = "Suggested",
}

export interface FieldChange {
    /**
     * Name of the entity field that changed.
     */
    name?: string;
    /**
     * New value of the field. Note that this is a JSON string and use the corresponding field
     * type to deserialize it.
     */
    newValue?: any;
    /**
     * Previous value of the field. Note that this is a JSON string and use the corresponding
     * field type to deserialize it.
     */
    oldValue?: any;
}

/**
 * UI placement hints for scoping the resource.
 */
export interface ResourceContext {
    /**
     * Optional component-level anchor within the page.
     */
    componentId?: string;
    /**
     * Stable identifier for the product page (e.g., glossary, domains).
     */
    pageId: string;
    /**
     * Relative ordering weight when multiple resources match a context.
     */
    priority?: number;
}

/**
 * Suggested proficiency tier for the resource.
 */
export enum ResourceDifficulty {
    Advanced = "Advanced",
    Intermediate = "Intermediate",
    Intro = "Intro",
}

/**
 * This schema defines the EntityReferenceList type used for referencing an entity.
 * EntityReference is used for capturing relationships from one entity to another. For
 * example, a table has an attribute called database of type EntityReference that captures
 * the relationship of a table `belongs to a` database.
 *
 * This schema defines the EntityReference type used for referencing an entity.
 * EntityReference is used for capturing relationships from one entity to another. For
 * example, a table has an attribute called database of type EntityReference that captures
 * the relationship of a table `belongs to a` database.
 */
export interface EntityReference {
    /**
     * If true the entity referred to has been soft-deleted.
     */
    deleted?: boolean;
    /**
     * Optional description of entity.
     */
    description?: string;
    /**
     * Display Name that identifies this entity.
     */
    displayName?: string;
    /**
     * Fully qualified name of the entity instance. For entities such as tables, databases
     * fullyQualifiedName is returned in this field. For entities that don't have name hierarchy
     * such as `user` and `team` this will be same as the `name` field.
     */
    fullyQualifiedName?: string;
    /**
     * Link to the entity resource.
     */
    href?: string;
    /**
     * Unique identifier that identifies an entity instance.
     */
    id: string;
    /**
     * If true the relationship indicated by this entity reference is inherited from the parent
     * entity.
     */
    inherited?: boolean;
    /**
     * Name of the entity instance.
     */
    name?: string;
    /**
     * Entity type/class name - Examples: `database`, `table`, `metrics`, `databaseService`,
     * `dashboardService`...
     */
    type: string;
}

/**
 * Kind of learning asset represented.
 */
export enum ResourceType {
    Article = "Article",
    Storylane = "Storylane",
    Video = "Video",
}

/**
 * Embedding configuration for rendering the resource inline.
 */
export interface ResourceSource {
    /**
     * Provider-specific configuration passed to the UI.
     */
    embedConfig?: { [key: string]: any };
    /**
     * Origin system that hosts the resource (Storylane, YouTube, etc.).
     */
    provider?: string;
    /**
     * Canonical URL.
     */
    url: string;
}

/**
 * Lifecycle state for controllable publishing.
 */
export enum Status {
    Active = "Active",
    Deprecated = "Deprecated",
    Draft = "Draft",
}

/**
 * This schema defines the type for labeling an entity with a Tag.
 */
export interface TagLabel {
    /**
     * Timestamp when this tag was applied in ISO 8601 format
     */
    appliedAt?: Date;
    /**
     * Who it is that applied this tag (e.g: a bot, AI or a human)
     */
    appliedBy?: string;
    /**
     * Description for the tag label.
     */
    description?: string;
    /**
     * Display Name that identifies this tag.
     */
    displayName?: string;
    /**
     * Link to the tag resource.
     */
    href?: string;
    /**
     * Label type describes how a tag label was applied. 'Manual' indicates the tag label was
     * applied by a person. 'Derived' indicates a tag label was derived using the associated tag
     * relationship (see Classification.json for more details). 'Propagated` indicates a tag
     * label was propagated from upstream based on lineage. 'Automated' is used when a tool was
     * used to determine the tag label.
     */
    labelType: LabelType;
    /**
     * Name of the tag or glossary term.
     */
    name?: string;
    /**
     * An explanation of why this tag was proposed, specially for autoclassification tags
     */
    reason?: string;
    /**
     * Label is from Tags or Glossary.
     */
    source: TagSource;
    /**
     * 'Suggested' state is used when a tag label is suggested by users or tools. Owner of the
     * entity must confirm the suggested labels before it is marked as 'Confirmed'.
     */
    state:  State;
    style?: Style;
    tagFQN: string;
}

/**
 * Label type describes how a tag label was applied. 'Manual' indicates the tag label was
 * applied by a person. 'Derived' indicates a tag label was derived using the associated tag
 * relationship (see Classification.json for more details). 'Propagated` indicates a tag
 * label was propagated from upstream based on lineage. 'Automated' is used when a tool was
 * used to determine the tag label.
 */
export enum LabelType {
    Automated = "Automated",
    Derived = "Derived",
    Generated = "Generated",
    Manual = "Manual",
    Propagated = "Propagated",
}

/**
 * Label is from Tags or Glossary.
 */
export enum TagSource {
    Classification = "Classification",
    Glossary = "Glossary",
}

/**
 * 'Suggested' state is used when a tag label is suggested by users or tools. Owner of the
 * entity must confirm the suggested labels before it is marked as 'Confirmed'.
 */
export enum State {
    Confirmed = "Confirmed",
    Suggested = "Suggested",
}

/**
 * UI Style is used to associate a color code and/or icon to entity to customize the look of
 * that entity in UI.
 */
export interface Style {
    /**
     * Hex Color Code to mark an entity such as GlossaryTerm, Tag, Domain or Data Product.
     */
    color?: string;
    /**
     * Cover image configuration for the entity.
     */
    coverImage?: CoverImage;
    /**
     * An icon to associate with GlossaryTerm, Tag, Domain or Data Product.
     */
    iconURL?: string;
}

/**
 * Cover image configuration for the entity.
 *
 * Cover image configuration for an entity. This is used to display a banner or header image
 * for entities like Domain, Glossary, Data Product, etc.
 */
export interface CoverImage {
    /**
     * Position of the cover image in CSS background-position format. Supports keywords (top,
     * center, bottom) or pixel values (e.g., '20px 30px').
     */
    position?: string;
    /**
     * URL of the cover image.
     */
    url?: string;
}
